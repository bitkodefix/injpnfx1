export const generateInvoiceNumber = (orderId: string, createdAt: string): string => {
  const date = new Date(createdAt);
  const year = date.getFullYear().toString().slice(-2);
  const month = (date.getMonth() + 1).toString().padStart(2, '0');
  const day = date.getDate().toString().padStart(2, '0');
  
  // Use last 4 characters of order ID for uniqueness
  const orderSuffix = orderId.slice(-4).toUpperCase();
  
  return `INV-${year}${month}${day}-${orderSuffix}`;
};

export const formatInvoiceDate = (dateString: string): string => {
  return new Date(dateString).toLocaleDateString('id-ID', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });
};

export const formatInvoiceDateTime = (dateString: string): string => {
  return new Date(dateString).toLocaleDateString('id-ID', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  });
};